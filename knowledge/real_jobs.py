def get_jobs():
    """Get all real jobs."""
    return REAL_JOBS


def get_jobs_by_type(job_type: str = None, engagement: str = None):
    """Filter jobs by type or engagement."""
    jobs = REAL_JOBS

    if job_type:
        jobs = [j for j in jobs if j["company_type"].lower() == job_type.lower()]

    if engagement:
        jobs = [j for j in jobs if j["engagement"].lower() == engagement.lower()]

    return jobs


